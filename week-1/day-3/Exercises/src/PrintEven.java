public class PrintEven {
    public static void main(String[] args) {
        int maximum = 500;
        System.out.println(maximum);
        for(int i=1; i <= maximum; i++){
            if (i%2==0){
                System.out.print(i + " ");
            }
        }
        // Create a program that prints all the even numbers between 0 and 500
    }
}
